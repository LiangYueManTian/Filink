package com.fiberhome.filink.demoserver.bean;


/**
 * 自建Bean 示例
 *
 * @author 姚远
 */
public class Demo {

    /**
     * 示例属性 名称
     */
    private String name;

    /**
     * 示例属性 年龄
     */
    private Integer age;

    /**
     * 示例属性 数量
     */
    private Long count;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Integer getAge() {
        return age;
    }

    public void setAge(Integer age) {
        this.age = age;
    }

    public Long getCount() {
        return count;
    }

    public void setCount(Long count) {
        this.count = count;
    }
}
